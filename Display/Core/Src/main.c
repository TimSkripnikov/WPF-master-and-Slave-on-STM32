typedef struct 
{
  uint8_t button1;
  uint8_t button2;

} StateButtons;

StateButtons state_button;

typedef enum {
    SCREEN_HELLO_TEXT,
    SCREEN_ROSE,
    SCREEN_ROSE2,
    SCREEN_TREE,
    SCREEN_MOPS
} Screen_t;

Screen_t current_screen = SCREEN_HELLO_TEXT;

void DisplayCurrentScreen() {
    SH1106_Clear();
    
    switch(current_screen) {
        case SCREEN_HELLO_TEXT:
            SH1106.CurrentX = 0;
            SH1106.CurrentY = 0;
            SH1106_Puts("  Welcome!", &Font_7x10, SH1106_COLOR_WHITE);
            SH1106_UpdateScreen(SH1106_Buffer);
            break;

        case SCREEN_ROSE:
            Convert_Bitmap_To_Pages(rose_map, image_buffer, SH1106_WIDTH, SH1106_HEIGHT);
            SH1106_Display_Image();
            break;
            
        case SCREEN_ROSE2:
            Convert_Bitmap_To_Pages(rose2_map, image_buffer, SH1106_WIDTH, SH1106_HEIGHT);
            SH1106_Display_Image();
            break;
            
        case SCREEN_TREE:
            Convert_Bitmap_To_Pages(tree_map, image_buffer, SH1106_WIDTH, SH1106_HEIGHT);
            SH1106_Display_Image();
            break;
            
        case SCREEN_MOPS:
            Convert_Bitmap_To_Pages(mops_map, image_buffer, SH1106_WIDTH, SH1106_HEIGHT);
            SH1106_Display_Image();
            break;
    }
}
